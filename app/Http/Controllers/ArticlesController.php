     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Article $articles)
    {
        //
        $articles->update($request->all());
        
        return redirect('articles');
    }

    /**
